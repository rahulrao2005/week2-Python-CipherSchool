fruits = ["orange", "apple", "pear","straw"]

# for loops
for i in fruits:
    print(i)
    
    

# while loop
fruits = ["orange", "apple", "pear","straw"]
i= 0
while i<len(fruits):
    print(fruits[i])
    i += 1   

